function hashString(input) {
  let hash = 2166136261;
  for (let i = 0; i < input.length; i += 1) {
    hash ^= input.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

function pick(seed, list, offset = 0) {
  return list[(seed + offset) % list.length];
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function createFishSpecies(name, location, index) {
  const seed = hashString(`${location.id}:${name}`);
  const maxWeight = maxWeightOverrides[name] ?? clamp(1 + (seed % 45), 1, 70);
  const tier = location.difficulty;
  const rareName = name.includes("rare");
  const heavyBonus = Math.sqrt(maxWeight) * 14;
  const baseValue = clamp(Math.round(5 + heavyBonus + tier * 8 + (rareName ? 75 : 0)), 5, 500);
  const valuePerKg = clamp(Math.round(1 + Math.log2(maxWeight + 1) * 6 + tier * 1.5), 1, 50);
  const medalChance = clamp(0.035 + tier * 0.006 + (maxWeight > 40 ? 0.018 : 0), 0.03, 0.16);
  const canBeUnique = uniqueCapable.has(name);
  const uniqueChance = canBeUnique
    ? clamp(0.0025 + tier * 0.0013 + (rareName ? 0.005 : 0), 0.002, 0.025)
    : 0;
  const guide = fishGuideOverrides[location.id]?.[name];
  const guidedLures = lureTypesForGuideTerms([...(guide?.best ?? []), ...(guide?.ok ?? [])]);
  const fallbackLures = [
    pick(seed, lureTypes),
    pick(seed, lureTypes, 3),
    maxWeight < 1 ? "natural" : pick(seed, lureTypes, 5),
  ];

  return {
    id: `${location.id}_${index}_${name.toLowerCase().replace(/[^a-z0-9ąćęłńóśźż]+/gi, "_")}`,
    name,
    baseValue,
    valuePerKg,
    maxWeight,
    medalChance,
    uniqueChance,
    canBeUnique,
    behavior: pick(seed, behaviors),
    aggression: clamp(0.2 + ((seed >> 2) % 70) / 100, 0.2, 0.95),
    caution: clamp(0.15 + ((seed >> 5) % 80) / 100, 0.15, 0.95),
    intelligence: clamp(0.2 + ((seed >> 7) % 70) / 100, 0.2, 0.95),
    fearThreshold: clamp(0.22 + ((seed >> 11) % 62) / 100, 0.22, 0.84),
    activeHours: [
      (4 + (seed % 5)) % 24,
      (18 + ((seed >> 4) % 6)) % 24,
    ],
    weatherPreference: pick(seed, weatherPrefs, 2),
    currentPreference: clamp(((seed >> 9) % 100) / 100, 0.08, 0.9),
    preferredLures: [...new Set([...guidedLures, ...fallbackLures])].slice(0, 3),
    preferredColors: [pick(seed, colors, 1), pick(seed, colors, 4)],
    visualHue: (seed % 360),
  };
}

export const LOCATIONS = locationSeeds.map((location) => ({
  ...location,
  fish: fishNamesByLocation[location.id].map((name, index) => createFishSpecies(name, location, index)),
}));

function fallbackGuideForSpecies(species) {
  const best = (species?.preferredLures ?? []).slice(0, 2).map((type) => lureGuideLabels[type] ?? type);
  const ok = (species?.preferredLures ?? []).slice(2, 3).map((type) => lureGuideLabels[type] ?? type);
  return {
    best: best.length ? best : ["naturalne"],
    ok: ok.length ? ok : ["spokojne prowadzenie"],
    bad: species?.maxWeight > 18 ? ["zbyt małe przynęty"] : ["zbyt ciężkie przynęty"],
    note: "",
  };
}

export function getFishGuide(locationId, speciesName) {
  const location = LOCATIONS.find((item) => item.id === locationId);
  const species = location?.fish.find((fish) => fish.name === speciesName);
  const override = fishGuideOverrides[locationId]?.[speciesName];
  return {
    ...fallbackGuideForSpecies(species),
    ...(override ?? {}),
  };
}

export function getLocationGuideNote(locationId) {
  return locationGuideNotes[locationId] ?? "";
}

