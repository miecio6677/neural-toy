function lureTypesForGuideTerms(terms = []) {
  const text = terms.join(" ").toLowerCase();
  const types = [];
  if (/(kukurydza|chleb|robak|mięso|roślinne|naturalne|plankton|lekki zestaw|noc)/i.test(text)) types.push("natural");
  if (/(spinner|obrot)/i.test(text)) types.push("spinner");
  if (/(wobler|wobbl|crank)/i.test(text)) types.push("crankbait");
  if (/(guma|jig)/i.test(text)) types.push("jig");
  if (/(mała ryba|duża ryba|bait fish|rybka|minnow)/i.test(text)) types.push("minnow");
  if (/(szybkie|ciężkie|spoon|wahad)/i.test(text)) types.push("spoon");
  if (/(surface|powierzch|popper)/i.test(text)) types.push("popper");
  return [...new Set(types)];
}

